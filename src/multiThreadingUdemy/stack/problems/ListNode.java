package multiThreadingUdemy.stack.problems;

public class ListNode {
	
	private char data;
	private ListNode next;
	
	public char getData() {
		return data;
	}
	public void setData(char data) {
		this.data = data;
	}
	public ListNode getNext() {
		return next;
	}
	public void setNext(ListNode next) {
		this.next = next;
	}
	

}
